package com.bean.institute;

public class InstituteBean {
	
	private String iId;
	private String iName;
	private long iContact;
	private String iMail;
	private String iCity;
	private String iState;
	
	
	public String getiId() {
		return iId;
	}
	public void setiId(String iId) {
		this.iId = iId;
	}
	public String getiName() {
		return iName;
	}
	public void setiName(String iName) {
		this.iName = iName;
	}
	public long getiContact() {
		return iContact;
	}
	public void setiContact(long iContact) {
		this.iContact = iContact;
	}
	public String getiMail() {
		return iMail;
	}
	public void setiMail(String iMail) {
		this.iMail = iMail;
	}
	public String getiCity() {
		return iCity;
	}
	public void setiCity(String iCity) {
		this.iCity = iCity;
	}
	public String getiState() {
		return iState;
	}
	public void setiState(String iState) {
		this.iState = iState;
	}

}
